package FirstPackage;

public class FindElemArray {
	public static void main(String[] args) {
	int[]array1= {2,4,6,5,7};
	String[]array2= {"aarthi","sas","arun"};
	int element=7;
	String element1="arun";

	for(int num:array1) {
		if(num==element) {
			System.out.println(element+"   is present in array1");
		}}
	for( String name1:array2) {
		if(name1==element1) {
			System.out.println(element1+"   is present in array2");
		}}
}}
